#include "libft.h"

int		ft_ishexa(char c)
{
	char	*str;
	int		i;

	i = -1;
	str = "0123456789ABCDEFabcdefx";
	while (str[++i])
		if (str[i] == c)
			return (1);
	return (0);
}
