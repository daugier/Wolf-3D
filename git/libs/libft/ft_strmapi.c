#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	char	*s2;
	int		i;

	i = 0;
	if (!s)
		return (NULL);
	if (!(s2 = malloc(sizeof(char) * ft_strlen(s) + 1)) || !s)
		return (NULL);
	while (s[i])
	{
		s2[i] = f(i, s[i]);
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
